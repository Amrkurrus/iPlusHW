regions = ("Алматинская", "Жамбыльская", "Кызылординская", "Туркестанская", "Восточно-Казахстанская", "Акмолинская", "Костанайская", "Северо-Казахстанская")

print("Введите площадь в гектарах.")
sum_count = 0
for i in regions:
    a = int(input(f"{i} область: "))
    sum_count += a
print(f"Общая сумма: {sum_count}")
print(f"Среднее по облостям: {sum_count/len(regions):.1f}")